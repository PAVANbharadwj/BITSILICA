/************** ITM printf **************/
void debug_printf(const char *fmt, ...)
{
    char buffer[256];
    va_list args;
    va_start(args, fmt);
    vsnprintf(buffer, sizeof(buffer), fmt, args);
    va_end(args);

    for (uint16_t i = 0; buffer[i] != '\0'; i++)
    {
        ITM_SendChar(buffer[i]);
    }
}

/************** FreeRTOS Delay **************/
void rtos_delay(uint32_t delay_ms)
{
    vTaskDelay(pdMS_TO_TICKS(delay_ms));
}

/************** Task 1: LED Toggle **************/
/************** Task 1: LED Toggle Task **************/
void led_Task_Handler(void *params)
{
    debug_printf("LED Task running\n");

    while (1)
    {
        HAL_GPIO_TogglePin(LD2_GPIO_Port, LD2_Pin);
        vTaskDelay(pdMS_TO_TICKS(500));
    }
}

/************** Task 2: Button Monitor Task **************/
void btn_Task_Handler(void *params)
{
    debug_printf("Button Task running\n");

    while (1)
    {
        uint8_t button = HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_13);  // Active low

        if (button == GPIO_PIN_RESET)   // Button pressed
        {
            debug_printf("Button pressed â†’ Stopping LED Task\n");

            // DELETE LED TASK
            vTaskDelete(xTaskHandler1);

            // Optionally turn LED off permanently
            HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);

            // After stopping LED task, this task continues running
            // Add small debounce delay
            vTaskDelay(pdMS_TO_TICKS(300));
        }

        vTaskDelay(pdMS_TO_TICKS(50));
    }
}


